
public class Zanahoria {
	private double peso;
    private double energy;
    
    Zanahoria(double peso,double energia){
    	this.setPeso(peso);
    	this.energy=energia;
    }

	

	public double getPeso() {
		return peso;
	}



	public void setPeso(double peso) {
		this.peso = peso;
	}



	public double getEnergy() {
		return energy;
	}

	public void setEnergy(double energy) {
		this.energy = energy;
	}
    
}
